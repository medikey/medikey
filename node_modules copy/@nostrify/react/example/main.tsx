/** @jsxRuntime automatic *//** @jsxImportSource npm:react@^18.0.0 *//** @jsxImportSourceTypes npm:@types/react@^18.0.0 *//** @jsxFactory React.createElement *//** @jsxFragmentFactory React.Fragment */import { NostrLoginProvider } from 'jsr:@nostrify/react@^0.2.9/login';
import { QueryClient, QueryClientProvider } from 'npm:@tanstack/react-query@^5.69.0';
import { StrictMode, Suspense } from 'npm:react@^18.0.0';
import ReactDOM from 'npm:/react-dom@^18.0.0/client';

import App from './App.tsx';
import NostrProvider from './NostrProvider.tsx';

const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      refetchOnWindowFocus: false,
      staleTime: 60000,
      gcTime: Infinity,
    },
  },
});

ReactDOM.createRoot(document.getElementById('root') as HTMLElement).render(
  <StrictMode>
    <QueryClientProvider client={queryClient}>
      <NostrLoginProvider storageKey='myapp'>
        <NostrProvider relays={['wss://ditto.pub/relay']}>
          <Suspense>
            <App />
          </Suspense>
        </NostrProvider>
      </NostrLoginProvider>
    </QueryClientProvider>
  </StrictMode>,
);
