/** @jsxRuntime automatic *//** @jsxImportSource npm:react@^18.0.0 *//** @jsxImportSourceTypes npm:@types/react@^18.0.0 *//** @jsxFactory React.createElement *//** @jsxFragmentFactory React.Fragment */import { NostrEvent, NPool, NRelay1 } from 'jsr:@nostrify/nostrify@^0.46.4';
import { NostrContext } from 'jsr:@nostrify/react@^0.2.8';
import { type FC, type ReactNode, useRef } from 'npm:react@^18.0.0';

interface NostrProviderProps {
  children: ReactNode;
  relays: `wss://${string}`[];
}

const NostrProvider: FC<NostrProviderProps> = (props) => {
  const { children, relays } = props;

  const pool = useRef<NPool>(undefined);

  if (!pool.current) {
    pool.current = new NPool({
      open(url: string) {
        return new NRelay1(url);
      },
      reqRouter(filters) {
        return new Map(relays.map((url) => [url, filters]));
      },
      eventRouter(_event: NostrEvent) {
        return relays;
      },
    });
  }

  return (
    <NostrContext.Provider value={{ nostr: pool.current }}>
      {children}
    </NostrContext.Provider>
  );
};

export default NostrProvider;
